package question2;
import java.util.Scanner;
public class ProductAndDivision {
	public static void main(String[] args) {
		int a;
		int b;
		Scanner sc=new Scanner(System.in);
		a=sc.nextInt();
		b=sc.nextInt();
		System.out.println(+(a*b));
		System.out.println(+(a/b));
		System.out.println(+(a%b));
	}

}
